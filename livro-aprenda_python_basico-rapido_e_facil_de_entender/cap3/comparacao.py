# IGUAL A
print(4==5) # FALSE
print(4==4) # TRUE

# DIFERENTE DE
# !=
print(4!=5) # TRUE
print(4!=4) # FALSE

# MAIOR QUE
# >
print(4>5) # FALSE
print(4>4) # FALSE

# MAIOR OU IGUAL
# >=
print(4>=5) # FALSE
print(4>=4) # TRUE

# MENOR QUE
# <
print(4<5) # TRUE
print(4<4) # FALSE

# MENOR OU IGUAL
# <=
print(4<=5) # TRUE
print(4<=4) # TRUE